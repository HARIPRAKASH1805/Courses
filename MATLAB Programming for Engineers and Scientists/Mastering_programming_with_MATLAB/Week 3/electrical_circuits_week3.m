m11=(R(1)*R(2) + R(1)*R(7) + R(2)*R(7));
m12=-(R(1)*R(2));
m13=0;
m21=-(R(3)*R(4)*R(8));
m22=(R(4)*R(7)*R(8) + R(3)*R(4)*R(8) + R(3)*R(7)*R(8) + R(3)*R(4)*R(7));
m23=-(R(3)*R(4)*R(7));
m31=0;
m32=-(R(5)*R(6));
m33=(R(6)*R(8) + R(5)*R(6) + R(5)*R(8));
M=[m11,m12,m13;m21,m22,m23;m31,m32,m33];
b11=V.*(R(2)*R(7));
b21=V.*(R(4)*R(7)*R(8));
b31=V.*(R(6)*R(8));
b=[b11;b21;b31];
y=M\b;
%{
R = [1,2,4,5,13,4,8,1];
V = 10;
voltage(V,R)
%}
end

