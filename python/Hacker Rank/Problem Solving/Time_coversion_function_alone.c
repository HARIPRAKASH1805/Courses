if(s[n-2]=='A')
{
    if((s[0]=='1')&&(s[1]=='2'))
    {
        s[0]=s[1]='0';
    }
}
if(s[n-2]=='P')
{
    if((s[0]=='0')&&(s[1]<'8'))
    {
    s[0]='1';
    s[1]=s[1]+2;
    }
    if((s[0]=='0')&&(s[1]=='8'))
    {
        s[0]='2';
        s[1]='0';
    }
    if((s[0]=='0')&&(s[1]=='9'))
    {
        s[0]='2';
        s[1]='1';
    }
    if((s[0]=='1')&&(s[1]=='0'))
    {
        s[0]='2';
        s[1]='2';
    }
    if((s[0]=='1')&&(s[1]=='1'))
    {
        s[0]='2';
        s[1]='3';
    }
    if((s[0]=='1')&&(s[1]=='2'))
    {
        s[0]='1';
        s[1]='2';
    }
}

s[n-1]=s[n-2]='\0';
